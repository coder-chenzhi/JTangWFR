package org.jtang.frequency.gspan;

public class Fragments {

	private String node ;
	private String subpath ;
	private String fragment ;
	private int nodeNumber ;
	private int subNodeNumber ;
	private int tailNodeNumber ;
	private int fragmentcount ;
	private int subpathcount ;
	private double confident ;
	private String dfscode;
	public String getDfscode() {
		return dfscode;
	}
	public void setDfscode(String dfscode) {
		this.dfscode = dfscode;
	}
	public String getNode() {
		return node;
	}
	public void setNode(String node) {
		this.node = node;
	}
	public String getSubpath() {
		return subpath;
	}
	public void setSubpath(String subpath) {
		this.subpath = subpath;
	}
	public String getFragment() {
		return fragment;
	}
	public void setFragment(String fragment) {
		this.fragment = fragment;
	}
	public int getNodeNumber() {
		return nodeNumber;
	}
	public void setNodeNumber(int nodeNumber) {
		this.nodeNumber = nodeNumber;
	}
	public int getSubNodeNumber() {
		return subNodeNumber;
	}
	public void setSubNodeNumber(int subNodeNumber) {
		this.subNodeNumber = subNodeNumber;
	}
	public int getTailNodeNumber() {
		return tailNodeNumber;
	}
	public void setTailNodeNumber(int tailNodeNumber) {
		this.tailNodeNumber = tailNodeNumber;
	}
	public int getFragmentcount() {
		return fragmentcount;
	}
	public void setFragmentcount(int fragmentcount) {
		this.fragmentcount = fragmentcount;
	}
	public int getSubpathcount() {
		return subpathcount;
	}
	public void setSubpathcount(int subpathcount) {
		this.subpathcount = subpathcount;
	}
	public double getConfident() {
		return confident;
	}
	public void setConfident(double confident) {
		this.confident = confident;
	}
}
